the_weather = 'cloudy'
 
case the_weather
when 'cloudy'
  puts "Bring an umbrella!"
when 'sunny'
  puts "Wear shorts and and t-shirt!"
when 'foggy'
  puts "Wear a light jacket!"
when 'rainy'
  puts "Wear a raincoat!"
when 'snowy'
    puts "Wear a warm coat!"
when 'windy'
    puts "Wear a hat!"
else
  puts "Hmm, well I don't know what you should wear!"
end